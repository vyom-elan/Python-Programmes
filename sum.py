#arithmetic operations in python
num1=input('Enter first number')
num2=input('Enter the second number')
num3=float(num1)+float(num2)
num4=float(num1)-float(num2)
num5=float(num1)*float(num2)
num6=float(num1)/float(num2)
num7=float(num1)//float(num2)
num8=float(num1)**float(num2)
num9=float(num1)%float(num2)
print('Sum of the two numbers {0},{1} is {2}'.format(num1,num2,num3))
print('Difference of the two numbers {0},{1} is {2}'.format(num1,num2,num4))
print('Product of the two numbers {0},{1} is {2}'.format(num1,num2,num5))
print('Division of the two numbers {0},{1} is {2}'.format(num1,num2,num6))
print('Division of the two numbers {0},{1}  rounded off to lowest integer is {2}'.format(num1,num2,num7))
print('Exponential of the two numbers {0},{1} is {2}'.format(num1,num2,num8))
print('Modulus of the two numbers {0},{1} is {2}'.format(num1,num2,num9))


